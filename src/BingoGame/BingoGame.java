    private void remov(int ele){
        for(int i=0;i<5;i++)
            for(int j=0;j<5;j++){
                if(a[i][j]==ele)
                    a[i][j]=0;
                if(b[i][j]==ele)
                    b[i][j]=0;
            }
        display();
        isWinner();
        cancelField.setText(null);
        cancelField.setEditable(true);
    }
    private void rowFull(int row){
        int ar=0,br=0,i;
        for(i=0;i<5;i++){
            if(a[row][i]==0)
                ar++;
            if(b[row][i]==0)
                br++;
        }
        if(ar==5)
            bingooA++;
        if(br==5)
            bingooB++;
    }
    private void colFull(int col){
        int ac=0,bc=0,i;
        for(i=0;i<5;i++){
            if(a[i][col]==0)
                ac++;
            if(b[i][col]==0)
                bc++;
        }
        if(ac==5)
            bingooA++;
        if(bc==5)
            bingooB++;
    }
    private void digFull(){
        int ad=0,bd=0,i;
        for(i=0;i<5;i++){
            if(a[i][i]==0)
                ad++;
            if(b[i][i]==0)
                bd++;
        }
        if(ad==5)
            bingooA++;
        if(bd==5)
            bingooB++;
        
    }
    private void isWinner(){
        int i,j;
        bingooA=0;
        bingooB=0;
        for(i=0;i<5;i++)
            rowFull(i);
        for(j=0;j<5;j++)    
            colFull(j);
        
        digFull();
        tf1.setText(String.valueOf(bingooA));
        tf2.setText(String.valueOf(bingooB));
        if(bingooA>=5){
            ta1.setText("You Win!");
            ta2.setText("Better luck next time");
        }
        else if(bingooB>=5){
            ta2.setText("You Win!");
            ta1.setText("Better luck next time");
        }
        
    }
