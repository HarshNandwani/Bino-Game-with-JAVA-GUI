    private int getRandom(){
        return r.nextInt(26);
    }
    private boolean isPresent(int mat[][],int ele){
        for(int i=0;i<5;i++)
            for(int j=0;j<5;j++)
                if(mat[i][j]==ele)
                    return true;
        return false;
    }
    private void fillMat(int X[][]){
        int element,i,j;
        for(i=0;i<5;i++)
            for(j=0;j<5;j++){
                element=getRandom();
                while(isPresent(X,element))
                    element=getRandom();
                X[i][j]=element;
            }
                
    }
    private void display(){
        ta1.setText(null); 
        ta1.setText("\n");
        ta2.setText(null); 
        ta2.setText("\n");
        for(int i=0;i<5;i++){
            for(int j=0;j<5;j++){
                if(a[i][j]<10 & a[i][j]!=0)
                    ta1.append("0"+a[i][j]);
                else
                    ta1.append(String.valueOf(a[i][j]));
                if(b[i][j]<10 & b[i][j]!=0)
                    ta2.append("0"+b[i][j]);
                else
                    ta2.append(String.valueOf(b[i][j]));
                if(j!=4){
                    ta1.append("   ");
                    ta2.append("   ");
                    if(a[i][j]==0)
                        ta1.append(" ");
                    if(b[i][j]==0)
                        ta2.append(" ");
                }
            }
            ta1.append("\n");
            ta2.append("\n");
        }
           
    }
    private void gameStart(){
        lable.setText("Game Begins!");
        for(int i=1;i<=625;i++){
            cancelField.setText(null);
            if(i%2!=0){
                lable.setText("player 1"+"'s choice");
                cancelField.setEditable(true);
                
            }
            else{
                lable.setText("player 2"+"'s choice");
                cancelField.setEditable(true);
                
            }
        isWinner();
        }
    }
